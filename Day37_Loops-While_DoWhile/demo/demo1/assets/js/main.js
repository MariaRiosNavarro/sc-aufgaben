// do while & while

// do while - simple sintaxEr gibt immer mindestens ein mal egal dass der bedienung nicht trift,
// zb let coins = 7, wird 8 ausgeben und dann abrechen

let coins = 0;

do {
  coins++;
  console.log(coins);
} while (coins < 5);

// while
// simple syntax

while (coins < 5) {
  coins++;
  console.log(coins);
}
